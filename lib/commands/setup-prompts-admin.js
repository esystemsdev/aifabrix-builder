/**
 * Admin credential prompts for `aifabrix setup` (dev / pro installation profiles).
 *
 * @fileoverview Setup installation profile + password bundle prompts
 * @author AI Fabrix Team
 * @version 1.0.0
 */

'use strict';

const chalk = require('chalk');
const inquirer = require('inquirer');
const logger = require('../utils/logger');
const config = require('../core/config');
const { generateProAdminPasswords } = require('../core/admin-password-generator');
const {
  singlePasswordBundle,
  splitPasswordBundle
} = require('../core/admin-secrets-bundle');
const {
  INSTALLATION_PROFILE,
  normalizeInstallationProfile,
  isProInstallationProfile
} = require('../core/setup-installation-profile');
const { validateEmail, validatePassword } = require('./setup-prompts-validators');

/**
 * Resolve installation profile from CLI options or config (interactive fallback in orchestrator).
 * @param {Object} [options]
 * @returns {Promise<'dev'|'pro'>}
 */
async function resolveInstallationProfileFromOptions(options = {}) {
  const cli = options.installation ?? options.installationProfile;
  if (cli !== undefined && cli !== null && String(cli).trim() !== '') {
    return normalizeInstallationProfile(cli);
  }
  try {
    return await config.getSetupInstallationProfile();
  } catch {
    return INSTALLATION_PROFILE.DEV;
  }
}

/**
 * @async
 * @returns {Promise<'dev'|'pro'>}
 */
async function promptInstallationProfile() {
  const { profile } = await inquirer.prompt([
    {
      type: 'list',
      name: 'profile',
      message: 'Installation profile:',
      choices: [
        {
          name: 'Development (dev) — one admin password for all services',
          value: INSTALLATION_PROFILE.DEV
        },
        {
          name: 'Production (pro) — autogenerate strong passwords or enter manually',
          value: INSTALLATION_PROFILE.PRO
        }
      ],
      default: INSTALLATION_PROFILE.DEV
    }
  ]);
  return profile;
}

/**
 * @param {Object} [options]
 * @returns {Promise<string>}
 */
async function promptAdminEmail(options = {}) {
  const cli = String(options.adminEmail || '').trim();
  if (cli) return cli;

  let defaultEmail;
  try {
    const saved = await config.getAdminEmail();
    defaultEmail = saved && saved.trim() ? saved.trim() : undefined;
  } catch {
    defaultEmail = undefined;
  }
  const emailQuestion = {
    type: 'input',
    name: 'adminEmail',
    message: 'Admin email (Keycloak / pgAdmin login):',
    validate: validateEmail
  };
  if (defaultEmail !== undefined) {
    emailQuestion.default = defaultEmail;
  }
  const { adminEmail } = await inquirer.prompt([emailQuestion]);
  return String(adminEmail).trim();
}

/**
 * @async
 * @returns {Promise<import('../core/admin-secrets-bundle').AdminPasswordBundleSingle>}
 */
async function promptDevPasswordBundle() {
  const answers = await inquirer.prompt([
    {
      type: 'password',
      name: 'adminPassword',
      message: 'Admin password (Postgres / Keycloak / pgAdmin / platform login):',
      mask: '*',
      validate: validatePassword
    },
    {
      type: 'password',
      name: 'adminPasswordConfirm',
      message: 'Confirm admin password:',
      mask: '*',
      validate(input, all) {
        if (input !== all.adminPassword) return 'Passwords do not match';
        return true;
      }
    }
  ]);
  return singlePasswordBundle(answers.adminPassword);
}

/**
 * Print pro autogenerated passwords once (never log plaintext elsewhere).
 * @param {{ infra: string, keycloak: string, platform: string }} passwords
 */
function displayProPasswordsOnce(passwords) {
  logger.log('');
  logger.log(chalk.bold.yellow('Save these passwords now — they will not be shown again.'));
  logger.log(chalk.gray('────────────────────────────────────────'));
  logger.log(`  Infra (Postgres / pgAdmin / Redis Commander): ${passwords.infra}`);
  logger.log(`  Keycloak install admin:                     ${passwords.keycloak}`);
  logger.log(`  Platform UI login (user admin):             ${passwords.platform}`);
  logger.log(chalk.gray('────────────────────────────────────────'));
  logger.log('');
}

/**
 * @param {Object} [options]
 * @returns {Promise<import('../core/admin-secrets-bundle').AdminPasswordBundleSingle|import('../core/admin-secrets-bundle').AdminPasswordBundleSplit>}
 */
async function promptProPasswordBundle(options = {}) {
  const modeCli = String(options.proPasswordMode || '').trim().toLowerCase();
  if (modeCli === 'autogen') {
    const generated = generateProAdminPasswords();
    if (options.quiet !== true) {
      displayProPasswordsOnce(generated);
    }
    return splitPasswordBundle(generated);
  }
  if (modeCli === 'manual' && String(options.adminPassword || '').trim()) {
    return singlePasswordBundle(String(options.adminPassword).trim());
  }

  const { method } = await inquirer.prompt([
    {
      type: 'list',
      name: 'method',
      message: 'How do you want to set admin passwords (pro installation)?',
      choices: [
        { name: 'Autogenerate strong passwords (recommended, shown once)', value: 'autogen' },
        { name: 'Enter one password manually (same for all roles)', value: 'manual-single' },
        { name: 'Enter separate passwords (infra / Keycloak / platform)', value: 'manual-split' }
      ],
      default: 'autogen'
    }
  ]);

  if (method === 'autogen') {
    const generated = generateProAdminPasswords();
    displayProPasswordsOnce(generated);
    return splitPasswordBundle(generated);
  }
  if (method === 'manual-split') {
    return promptProManualSplitBundle();
  }
  return promptDevPasswordBundle();
}

/**
 * @async
 * @returns {Promise<import('../core/admin-secrets-bundle').AdminPasswordBundleSplit>}
 */
async function promptProManualSplitBundle() {
  const answers = await inquirer.prompt([
    {
      type: 'password',
      name: 'infra',
      message: 'Infra password (Postgres / pgAdmin / Redis Commander):',
      mask: '*',
      validate: validatePassword
    },
    {
      type: 'password',
      name: 'keycloak',
      message: 'Keycloak install admin password:',
      mask: '*',
      validate: validatePassword
    },
    {
      type: 'password',
      name: 'platform',
      message: 'Platform UI login password (user admin):',
      mask: '*',
      validate: validatePassword
    }
  ]);
  return splitPasswordBundle(answers);
}

/**
 * @param {Object} [options]
 * @returns {Promise<{ adminEmail: string, passwordBundle: object, profile: 'dev'|'pro' }>}
 */
async function promptAdminCredentialsWithProfile(options = {}) {
  let profile = await resolveInstallationProfileFromOptions(options);
  const hasCliProfile =
    options.installation !== undefined ||
    options.installationProfile !== undefined;
  if (!hasCliProfile) {
    profile = await promptInstallationProfile();
  }
  await config.setSetupInstallationProfile(profile);

  const adminEmail = await promptAdminEmail(options);
  const passwordBundle = isProInstallationProfile(profile)
    ? await promptProPasswordBundle(options)
    : await promptDevPasswordBundle();

  return { adminEmail, passwordBundle, profile };
}

module.exports = {
  INSTALLATION_PROFILE,
  resolveInstallationProfileFromOptions,
  promptInstallationProfile,
  promptAdminEmail,
  promptDevPasswordBundle,
  promptProPasswordBundle,
  displayProPasswordsOnce,
  promptAdminCredentialsWithProfile
};
